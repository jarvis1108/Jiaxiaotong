package com.dxtwangxiao.strongestsuperscholar.config;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

/**
 * 微信小程序配置
 */
@Component
@ConfigurationProperties(prefix = "mp")
public class WechatConfiguration {

    private String appid;

    private String appSecret;

    private String mch_id;

    private String key;

    private String signType;

    private String tradeType;

    private String codeToOpenidUrl;

    private String unifiedOrderUrl;

    private String notifyUrl;

    private String questionForLibAppid;

    private String questionForLibAppSecret;

    private String backendAppid;

    private String backendAppSecret;

    private String authorizationUrl;

    private String accessTokenUrl;

    private String transfersUrl;

    private String certLocation;

    public String getAppid() {
        return appid;
    }

    public void setAppid(String appid) {
        this.appid = appid;
    }

    public String getAppSecret() {
        return appSecret;
    }

    public void setAppSecret(String appSecret) {
        this.appSecret = appSecret;
    }

    public String getMch_id() {
        return mch_id;
    }

    public void setMch_id(String mch_id) {
        this.mch_id = mch_id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getSignType() {
        return signType;
    }

    public void setSignType(String signType) {
        this.signType = signType;
    }

    public String getTradeType() {
        return tradeType;
    }

    public void setTradeType(String tradeType) {
        this.tradeType = tradeType;
    }

    public String getCodeToOpenidUrl() {
        return codeToOpenidUrl;
    }

    public void setCodeToOpenidUrl(String codeToOpenidUrl) {
        this.codeToOpenidUrl = codeToOpenidUrl;
    }

    public String getUnifiedOrderUrl() {
        return unifiedOrderUrl;
    }

    public void setUnifiedOrderUrl(String unifiedOrderUrl) {
        this.unifiedOrderUrl = unifiedOrderUrl;
    }

    public String getNotifyUrl() {
        return notifyUrl;
    }

    public void setNotifyUrl(String notifyUrl) {
        this.notifyUrl = notifyUrl;
    }

    public String getCertLocation() {
        return certLocation;
    }

    public void setCertLocation(String certLocation) {
        this.certLocation = certLocation;
    }

    public String getTransfersUrl() {
        return transfersUrl;
    }

    public void setTransfersUrl(String transfersUrl) {
        this.transfersUrl = transfersUrl;
    }

    public String getQuestionForLibAppid() {
        return questionForLibAppid;
    }

    public void setQuestionForLibAppid(String questionForLibAppid) {
        this.questionForLibAppid = questionForLibAppid;
    }

    public String getQuestionForLibAppSecret() {
        return questionForLibAppSecret;
    }

    public void setQuestionForLibAppSecret(String questionForLibAppSecret) {
        this.questionForLibAppSecret = questionForLibAppSecret;
    }

    public String getBackendAppid() {
        return backendAppid;
    }

    public void setBackendAppid(String backendAppid) {
        this.backendAppid = backendAppid;
    }

    public String getBackendAppSecret() {
        return backendAppSecret;
    }

    public void setBackendAppSecret(String backendAppSecret) {
        this.backendAppSecret = backendAppSecret;
    }

    public String getAuthorizationUrl() {
        return authorizationUrl;
    }

    public void setAuthorizationUrl(String authorizationUrl) {
        this.authorizationUrl = authorizationUrl;
    }

    public String getAccessTokenUrl() {
        return accessTokenUrl;
    }

    public void setAccessTokenUrl(String accessTokenUrl) {
        this.accessTokenUrl = accessTokenUrl;
    }
}
