package com.dxtwangxiao.strongestsuperscholar.config;

/**
 * 实现功能：针对Sesion分布式配置
 */
public class SessionDistributedCfg
{
    /**
     * 实现功能:针对Redis的配置
     */
    private RedisCfg objMyRedisCfg;

    /**
     * 实现功能：针对Session的配置
     */
    private SessionCfg objMySessionCfg;

    /**
     * 实现功能：构造函数
     * @param  objMyRedisCfg Redis相关配置
     * @param  objMySessionCfg Session的相关配置
     */
    public  SessionDistributedCfg(RedisCfg objMyRedisCfg, SessionCfg objMySessionCfg)
    {
        this.objMyRedisCfg = objMyRedisCfg;
        this.objMySessionCfg = objMySessionCfg;
    }

    /**
     * 实现功能：缺省无参构造函数
     */
    public  SessionDistributedCfg()
    {

    }

    public RedisCfg getObjMyRedisCfg()
    {
        return objMyRedisCfg;
    }

    public void setObjMyRedisCfg(RedisCfg objMyRedisCfg)
    {
        this.objMyRedisCfg = objMyRedisCfg;
    }

    public SessionCfg getObjMySessionCfg()
    {
        return objMySessionCfg;
    }

    public void setObjMySessionCfg(SessionCfg objMySessionCfg)
    {
        this.objMySessionCfg = objMySessionCfg;
    }
}
